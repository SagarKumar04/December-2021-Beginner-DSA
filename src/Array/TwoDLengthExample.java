package Array;

public class TwoDLengthExample {
    public static void main(String[] args) {
        int arr[][] = new int[9][5];
        int row = arr.length;
        int column = arr[0].length;

        System.out.println("Number of Rows: " + row);
        System.out.println("Number of Columns: " + column);
    }
}
